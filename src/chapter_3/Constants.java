package chapter_3;

public class Constants {

	public static void main(String[] args) {
		final int MAX_SIZE = 100; // 상수 MAX_SIZE를 선언
		final char CONST_CHAR = '상'; // 상수 CONST_CHAR를 선언
		final int CONST_ASSIGNED; // 상수 CONST_ASSIGNED를 선언
		CONST_ASSIGNED = 10;
		System.out.println("상수1 : " + MAX_SIZE);
		System.out.println("상수2 : " + CONST_CHAR);
		System.out.println("상수3 : " + CONST_ASSIGNED);
	}
}
